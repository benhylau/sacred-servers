 Four Sites of Endless Feeling
}

We've lived with the world wide web for over three decades, and with each year
the amount of places visited online diminishes, while the breadth of activities
we do on these platforms increases 10 fold. It seems like there's only handful
of sites today, where we've given the full spectrum of our human emotion.

On sites like Facebook, Instagram, and Twitter we post dumb jokes, sincere
fears, theories about the world, our arguments for hte existence of God, taco
truck reviews. We share art, art that no one sees, art that does well, art that
stays on the platform after the artist is gone; an eerily banal final post and a
now complete media tab. We announce that we're looking for a new job, network,
share carefully edited tossed off opinions hoping new eyes are watching,
impressed. Casually interview, announce new work, and subtly change how we talk
to our friends now that all our bosses are watching. We meet each other, flirt,
ask out, catch up, create worlds of inside jokes with bad reposts, corny memes,
and ironic replies. We fuck, we say I love you, we cheat, we break up, we block.
We painfully linger, with one segement of time on a facebook wall filling us
with memory like it's an old, unwashed shirt. and we stay together, share
apartment photos, merge timelines, announce engagements, plan weddings, share
invites, share baby photos, embarass our child online. We rage, we breakdown, we
organize, we ask for help, we witness and grieve, we scream about inustice into
what feels like a void. We intentionally stay quiet, or speak up, or cowardly
pause over the like button for the photo of a burger, afraid that liking it now
will be uncaring to the deep pain being posted above and below it. We ask for
medical help, for places to stay, for funding of our video series or for the
surgery we desperately need. We share something ordinary but know that we are
really in it asking for friendship, safety, an audience, change, to hurt, to be
noticed, for someone to step in, for nostalgic pangs and hoped for futures. We
share cat photos.

We post again and again to the same few platforms, some of it private and a lot
of it public, not necessarily meant for each other to see, the inarticulate
thing we are wanting out of all of this being shared, at once, to some big vast
other.
